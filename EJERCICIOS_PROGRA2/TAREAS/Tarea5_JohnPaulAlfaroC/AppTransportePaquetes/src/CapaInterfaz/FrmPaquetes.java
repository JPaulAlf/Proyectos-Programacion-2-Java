    private void llenarCombosCiudades() {
        //LLenar combo de ciudades de origen
        
        for (Ciudad ciudad : arrayCiudades) {
            jCboOrigen.addItem(ciudad);
        }
        
        for (Ciudad ciudad : arrayCiudades) {
            jCboDestino.addItem(ciudad);
        }
        
        this.jCboOrigen.setSelectedIndex(-1);
        this.jCboDestino.setSelectedIndex(-1);
    }
    
