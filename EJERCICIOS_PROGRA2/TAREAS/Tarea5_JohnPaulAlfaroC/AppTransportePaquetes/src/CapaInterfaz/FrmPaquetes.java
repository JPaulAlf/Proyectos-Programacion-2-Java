    private void jBtnValorarEnvioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnValorarEnvioActionPerformed
       DecimalFormat formatoNumero = new DecimalFormat("#,##0.00");
       

        if (this.jTxtPeso.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe digitar el peso del paquete");
            this.jTxtPeso.requestFocus();
            return;
        }

       
        
        if (this.jCboDestino.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar la ciudad destino");
            this.jCboDestino.requestFocus();
            return;
        }

        if (this.jCboOrigen.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar la ciudad origen");
            this.jCboOrigen.requestFocus();
            return;
        }

        

       
        double peso = Double.parseDouble(this.jTxtPeso.getText());
      

         //Construcci√≥n del paquete
        paquete = new Paquete(peso);
        
        if (this.jChkFrecuente.isSelected()) {
           paquete.agregarDescuento(Descuento.Cliente_Frecuente);
        }

        if (this.jChkOro.isSelected()) {
            paquete.agregarDescuento(Descuento.Ciudadano_Oro);
        }
        
       
        
        paquete.setCiudadOrigen((Ciudad)this.jCboOrigen.getSelectedItem());
        paquete.setCiudadDestino((Ciudad)this.jCboDestino.getSelectedItem());
     
        if(jRdbNoPerecedero.isSelected()){
                 paquete.setTipoContenido(TipoContenido.No_Perecedero);     
        }
        else{
            if(jRdbPerecedero.isSelected()){
                 paquete.setTipoContenido(TipoContenido.Perecedero);     
            }
             else{
                 JOptionPane.showMessageDialog(this, "Debe seleccionar el tipo del contenido");
                 return;
             }     
        }
        
       
        this.jTxtCostoTraslado.setText(formatoNumero.format(paquete.costoTraslado()));
        this.jTxtCostoContenido.setText(formatoNumero.format(paquete.costoTipoContenido()));
        this.jTxtDescuentos.setText(formatoNumero.format(paquete.totalDescuentos()));
        this.jTxtCostoTotal.setText(formatoNumero.format(paquete.costoTotal()));
        this.jTxaResultado.append(paquete.toString());
        this.jBtnAgregarContenedor.setEnabled(true);
        this.jBtnValorarEnvio.setEnabled(false);
    }//GEN-LAST:event_jBtnValorarEnvioActionPerformed

