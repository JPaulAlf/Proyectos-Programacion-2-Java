    private void llenarCombosCiudades() {
            for (Iterator<Ciudad> iterator = arrayCiudades.iterator(); iterator.hasNext();) {
            Ciudad next = iterator.next();
            jCboOrigen.addItem(next.getNombre());
            jCboDestino.addItem(next.getNombre());
        }
    }
    
