    private void jBtnValorarEnvioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnValorarEnvioActionPerformed
   double pesoProducto=0;
   Descuento desc= null;
   DecimalFormat df = new DecimalFormat("#,##0.00");
   // validacion de campos vacios o incorrectos
        if(this.jTxtPeso.getText().trim().equals("")){
             JOptionPane.showMessageDialog(this, "Debe digitar el peso del articulo");
            this.jTxtPeso.requestFocus();
            return;
        }
        try{
            pesoProducto= Double.parseDouble(this.jTxtPeso.getText());
        }catch(NumberFormatException numberFormatException){
             JOptionPane.showMessageDialog(this, "El pso del producto debe ser numérico");
              this.jTxtPeso.requestFocus();
                  this.jTxtPeso.selectAll();
                 return;
            }
         if(this.jCboDestino.getSelectedIndex()==-1){
          JOptionPane.showMessageDialog(this, "Debe seleccionar una ciudad destino");
          this.jCboDestino.requestFocus();
          return;   
        }
        if(this.jCboOrigen.getSelectedIndex()==-1){
          JOptionPane.showMessageDialog(this, "Debe seleccionar una ciudad origen");
          this.jCboOrigen.requestFocus();
          return;   
        }
        if(!this.jRdbPerecedero.isSelected() && !this.jRdbNoPerecedero.isSelected()){
        JOptionPane.showMessageDialog(this, "Debe seleccionar un tipo de contenido");
            return;  
       }
        
        // Captura de datos, para enviar a instancias y sets
        paquete = new Paquete(pesoProducto);
        
        //selecciono el tipo de descuento que se va aplicar
            if(this.jChkFrecuente.isSelected()){
            desc= Descuento.Cliente_Frecuente;
            paquete.agregarDescuento(desc);
        }   
        if(this.jChkOro.isSelected()){
            desc=Descuento.Ciudadano_Oro;
            paquete.agregarDescuento(desc);
        }
        // selecciono las ciudades
           for (Ciudad ciudad : arrayCiudades) {
            if(jCboDestino.getSelectedItem().equals(ciudad.getNombre())){
                paquete.setCiudadDestino(ciudad);
            }
            if(jCboOrigen.getSelectedItem().equals(ciudad.getNombre())){
                paquete.setCiudadOrigen(ciudad);
            }
        }   
        // selecciono el tipo de contenido de los paquetes
        TipoContenido tipoContenido=(jRdbNoPerecedero.isSelected())?TipoContenido.No_Perecedero
                :TipoContenido.Perecedero;
        paquete.setTipoContenido(tipoContenido);
        
        //Me encargo de mostrar los datos en los espacios en blanco
        this.jTxaResultado.setText(paquete.toString());
        this.jTxtCostoTraslado.setText("$"+ df.format(paquete.costoTraslado()));
        this.jTxtCostoContenido.setText("$"+df.format(paquete.costoTipoContenido()));
        this.jTxtDescuentos.setText("$"+df.format(paquete.totalDescuentos()));
        this.jTxtCostoTotal.setText("$"+df.format(paquete.costoTotal()));
        this.jBtnAgregarContenedor.setEnabled(true);
    }//GEN-LAST:event_jBtnValorarEnvioActionPerformed

