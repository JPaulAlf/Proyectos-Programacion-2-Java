      private void llenarTabla() {
        //Actualiza la tabla a partir del ArrayList del archivador que contiene los artículos       tablaModelo.setRowCount(0);
       this.modeloTabla.setRowCount(0);
       
          for (Articulo articulo: ArchivadorArticulo.getArrayArticulo()) {
              Object [] objeto = new Object[3];
              objeto[0]= articulo.getCodigo();
                      objeto[1]=articulo.getDescripcion();
                            objeto[2]=articulo.getPrecio();
                            this.modeloTabla.addRow(objeto);
                      }
        
        
        
    }

    private void desactivarBotones() {
        jBtnGuardar.setEnabled(false);
        jBtnEliminar.setEnabled(true);
        jBtnModificar.setEnabled(true);
        jBtnCancelar.setEnabled(true);
    }
    private void activarBotones() {
        jBtnGuardar.setEnabled(true);
        jBtnEliminar.setEnabled(false);
        jBtnModificar.setEnabled(false);
        jBtnCancelar.setEnabled(false);
    }
    
    private void Limpiar(){
        jTxtCodigo.setText("");
        jTxtDescripcion.setText("");
        jTxtPrecio.setText("");
        activarBotones();
    }
    
   
