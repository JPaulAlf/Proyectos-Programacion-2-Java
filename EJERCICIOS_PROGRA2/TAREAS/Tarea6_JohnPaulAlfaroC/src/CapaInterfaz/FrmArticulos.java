    private void jBtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarActionPerformed
        String codigo,descripcion;
        double precio;
        codigo = jTxtCodigo.getText();
        descripcion = jTxtDescripcion.getText();
        precio = Double.parseDouble(jTxtPrecio.getText());
       //Almacena el nuevo artículo en el Archivador de artículos
        if(codigo.equals("")){
            JOptionPane.showMessageDialog(this, "Ingresar un codigo");
            return;
        }
         if(descripcion.equals("")){
            JOptionPane.showMessageDialog(this, "Ingresar una descripcion correcta");
            return;
        }
          if(precio<0){
            JOptionPane.showMessageDialog(this, "Ingresar un codigo");
            return;
        }
          articulo= new Articulo(codigo, descripcion, precio);
          ArchivadorArticulo.agregarArticulo(articulo);
          
        llenarTabla();
        Limpiar();
    }//GEN-LAST:event_jBtnGuardarActionPerformed
