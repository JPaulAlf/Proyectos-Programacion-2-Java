    private void jBtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnModificarActionPerformed

       if(jTblArticulos.getSelectedRow()==-1){
           JOptionPane.showMessageDialog(this, "Favor seleccionar un objeto a modificar en la tabla");
            return;
       }
        String codigo = jTxtCodigo.getText();
        int posicion = ArchivadorArticulo.posicionArticulo(codigo);
        String descripcion = jTxtDescripcion.getText();
        double precio = Double.parseDouble(jTxtPrecio.getText());
        
       //Almacena el nuevo artículo en el Archivador de artículos
         if(descripcion.equals("")){
            JOptionPane.showMessageDialog(this, "Ingresar una descripcion correcta");
            return;
        }
          if(precio<0){
            JOptionPane.showMessageDialog(this, "Ingresar un precio valido");
            return;
        }
          Articulo articulo= new Articulo(codigo, descripcion, precio);
    
                               
        ArchivadorArticulo.modificarCiudad(posicion, articulo);
        
        this.llenarTabla();
          Limpiar();
        activarBotones();
        jTxtCodigo.setEnabled(true);
        Limpiar();
    }//GEN-LAST:event_jBtnModificarActionPerformed
