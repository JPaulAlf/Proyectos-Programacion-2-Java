    private void jBtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEliminarActionPerformed
    if(!jLstApartados.isSelectionEmpty()){
            //mÃ¡s de un articulo seleccionado
            if(jLstApartados.getSelectedIndices() != null){
                for (Articulo item : jLstApartados.getSelectedValuesList()) {
                    modeloOrigen.addElement(item);
                    modeloDestino.removeElement(item);
                }
            }else{
                modeloOrigen.addElement((Articulo)jLstApartados.getSelectedValue());
                modeloDestino.removeElement(jLstApartados.getSelectedValue());
            }
        }
       else{
           JOptionPane.showMessageDialog(null, "Debe seleccionar una ciudad de la lista");
       }
        
        
    }//GEN-LAST:event_jBtnEliminarActionPerformed
