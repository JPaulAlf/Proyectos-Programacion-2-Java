    private void llenarLista() {
         ArrayList<Articulo> listaArticulos = capaPersistencia.ArchivadorArticulo.getArrayArticulo();
        for (Articulo articulo : listaArticulos) {
            modeloOrigen.addElement(articulo);
        }
            
    }

