    private void llenarTabla() {
        this.modeloTabla.setRowCount(0);
         Object[] fila = new Object[4];
        //La tabla se llena con todos los apartados que estÃ¡n en el archivador de apartados
        for (Apartado apartado : ArchivadorApartado.getArrayApartado()) {
           
            fila[0]= Apartado.getContadorApartado();
                      fila[1]=apartado.getCliente().getNombre();
                            fila[2]=apartado.getCantidadAbonos();
                                fila[3]=apartado.getSaldo();
                                        this.modeloTabla.addRow(fila);
        }
    }
    
