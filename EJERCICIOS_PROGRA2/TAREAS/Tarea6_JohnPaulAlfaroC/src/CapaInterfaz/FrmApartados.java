    private void jBtnRealizarApartadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnRealizarApartadoActionPerformed
    if(this.jTxtNombre.equals("")){
        JOptionPane.showMessageDialog(this,"favor ingresar un nombre");
    }
    if(this.jTxtIdentificacion.equals("")){
        JOptionPane.showMessageDialog(this,"favor ingresar una identificacion ");
    }
   if(modeloDestino.size()<0){
      JOptionPane.showMessageDialog(this, "Debe haber seleccionado elementos a realizar apartado");
   }
       String nombre=this.jTxtNombre.getText();
       String identificacion=this.jTxtIdentificacion.getText();
       
       Cliente cliente = new Cliente(identificacion, nombre);
       apartado = new Apartado(cliente);
            for (int i = 0; i < modeloDestino.size(); i++) {
            apartado.agregarArticulo(modeloDestino.get(i));
        }
        limpiarCampos();
        llenarLista();
        desactivarBotones();
        jBbtnNuevoApartado.setEnabled(true);
        ArchivadorApartado.agregarApartado(apartado);
        llenarTabla();
       
    }//GEN-LAST:event_jBtnRealizarApartadoActionPerformed
