    private void jBtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnRegistrarActionPerformed
    // validaciones de campos vacios y opciones selecionadas para crear el usuario y guardarlo en el archivador
      if(this.validacionCamposNORMALES()==false){
          return;
      }
      
       if(!this.jRdbUsuarioCorporativo.isSelected() && !this.jRdbUsuarioResidencial.isSelected()){
          JOptionPane.showMessageDialog(this,"Favor seleccionar un tipo de cuenta a crear",
                  "ERROR ENCONTRADO:",JOptionPane.ERROR_MESSAGE);
            return;
        }
       try {
           // Validacion de la tarjeta por metodos separados   
           if(this.validacionTARJETA()){
               return;
           }
       } catch (ParseException ex) {  }
        
        // captura de datos en variables, ya que fueron validados previamente. y estan CORRECTOS....
        String nombre= this.jTxtNombreUsuario.getText();
                String paisOrigen=this.jTxtPaisOrigen.getText(); 
                        String correo=this.jTxtCorreoElectronico.getText();
                                String contrasenna=this.jTxtConstrasenna.getText();
                                        String numTarjeta=this.jTxtNumeroTarjeta.getText();
                                                String CVV=this.jTxtCVV.getText(); //******ATRIBUTO DE TARJETA*******
                                                        String mes=Integer.toString( (Integer) this.jSpnMes.getValue() );//******ATRIBUTO DE TARJETA*******
                                                                String anno=Integer.toString( (Integer) this.jSpnAnno.getValue() ); //******ATRIBUTO DE TARJETA*******
                                                                       TIPO_TARJETA tipoTarjeta= (TIPO_TARJETA) this.jCbxTipoTarjeta.getSelectedItem();//******ATRIBUTO DE TARJETA********
          
                                                                       // Creacion de la tarjeta para el usuario, ya que fue validada previamente
                                                                       tarjeta= new Usuario_Tarjeta(numTarjeta, CVV, mes, anno, tipoTarjeta);
                                                                       
        // valida si el usuario ya existe dentro de la base de datos
        if(this.jRdbUsuarioCorporativo.isSelected()){   // Si es seleccionado un usuario de TIPO Corporativo
            if(Validacion.validacionUsuarioNuevo(correo)){
            JOptionPane.showMessageDialog(this,"Favor agregar un usuario distinto, el ingresado ya existe como cliente", "ERROR ENCONTRADO:",JOptionPane.ERROR_MESSAGE);
            return;
            }
          
            // creacion de los usuarios y su respectivo guardado
          this.usuarioNew= new UsuarioCorporativo(nombre, correo, contrasenna, tarjeta);
          ((UsuarioCorporativo)this.usuarioNew).setPaisOrigen(new Usuario_PaisOrigen(paisOrigen));
          usuarioNew.getTarjeta().agregarPago(new Usuario_Pago( LocalDateTime.now(), usuarioNew.costoInscripcion() ) );
          
          if(this.validacionCamposPerfiles()==false){ // valida si se ingreso el nombre de los perfiles
                return;   
                }
          
          this.llenaArrayPerfiles();// Guarda los perfiles dentro del usuario
          Archivador_Usuarios.agregarUsuario(usuarioNew); // Guarda usuario dentro del arhivo, logra la persistencia
         // JOptionPane.showMessageDialog(this,"Usuario creado exitosamente\n"+usuarioNew.toString());
         
         // muestra los datos de la creacion de la cuenta en esta ventana emergente
          FrmVentana_Registro_Informacion ventanaEmergente = new FrmVentana_Registro_Informacion(usuarioNew);
                    ventanaEmergente.setLocationRelativeTo(null);
                    ventanaEmergente.setVisible(true);
            }
        
        
        if(this.jRdbUsuarioResidencial.isSelected()){  // Si es seleccionado un usuario de TIPO Residencial
             if(Validacion.validacionUsuarioNuevo(correo)){
            JOptionPane.showMessageDialog(this,"Favor agregar un usuario distinto, el ingresado ya existe como cliente", "ERROR ENCONTRADO:",JOptionPane.ERROR_MESSAGE);
            return;
            }
             this.usuarioNew= new UsuarioResidencial(nombre, correo, contrasenna, tarjeta);
              ((UsuarioResidencial)this.usuarioNew).setPaisOrigen(new Usuario_PaisOrigen(paisOrigen));
              usuarioNew.getTarjeta().agregarPago(new Usuario_Pago( LocalDateTime.now(), usuarioNew.costoInscripcion() ) );
              
             if(this.validacionCamposPerfiles()==false){ // valida si se ingreso el nombre de los perfiles
                return;   
                }
             
             this.llenaArrayPerfiles(); // Guarda los perfiles dentro del usuario
             Archivador_Usuarios.agregarUsuario(usuarioNew);// Guarda usuario dentro del arhivo, logra la persistencia
       
             // muestra los datos de la creacion de la cuenta en esta ventana emergente
              FrmVentana_Registro_Informacion ventanaEmergente = new FrmVentana_Registro_Informacion(usuarioNew);
                    ventanaEmergente.setLocationRelativeTo(null);
                     ventanaEmergente.setVisible(true);
        }
        this.limpiarCampos();
        
    }//GEN-LAST:event_jBtnRegistrarActionPerformed

