    private boolean validacionTARJETA() throws ParseException{
        String numTarjeta=this.jTxtNumeroTarjeta.getText();
        String CVV=this.jTxtCVV.getText();
        String mes=Integer.toString( (Integer) this.jSpnMes.getValue() );
        String anno=Integer.toString( (Integer) this.jSpnAnno.getValue() );
        TIPO_TARJETA tipoTarjeta= (TIPO_TARJETA) this.jCbxTipoTarjeta.getSelectedItem();
        
        if(!Validacion.validarFechaTarjeta(mes,anno)){
             JOptionPane.showMessageDialog(this,"Favor agregar una fecha valida de tarjeta");
            return true;  
        }
           if(!Validacion.validarNumeroTarjeta(numTarjeta)){
             JOptionPane.showMessageDialog(this,"Favor agregar un numero de tarjeta valido");
            return true;  
        }
              if(!Validacion.validarTipoTarjeta(numTarjeta, CVV, tipoTarjeta)){
             JOptionPane.showMessageDialog(this,"Favor agregar un tipo valido de tarjeta");
            return true;  
        }
           return false;
    }
